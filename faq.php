                      <li><i class="fas fa-asterisk"></i>Majority of master programs have  120 credit points (90 for courses(subjects) and 30 for thesis ) . If we take the duration as 4 semesters then it is 30 credits per semester.</li>
                      <li> <i class="fas fa-asterisk"></i>There is no rule like one needs to complete the course in four semesters. You can take more or less semesters to complete the course but there is an upper limit also after eight semesters your semester fee is higher. </li>
                      <li><i class="fas fa-asterisk"></i>What does “credit points” mean to a student apart from the fact that it is a passing criteria?</li>
                      <li> <i class="fas fa-asterisk"></i> The expectation is - for every credit point, you devote 30 working hours into the course.</li>
                                <li><h5>1 credit = 30 working hours.</h5></li>
                  </ul>

                  <ul class="list list-dark mt-4">
                      <h6>Example Scenario</h6>  
                      <li><i class="fas fa-asterisk"></i> Let’s say you are taking 30 credits per semester then 
                                 30 credits =  900 working hours in maximum 10 weeks for this semester. [Mid-April to End of June (best case scenario)] </li>
                      <li> <i class="fas fa-asterisk"></i>Roughly around 90 hours per week or 12.5 hours of study PER DAY( if we take Sunday as Rest day) </li>
                  </ul>

                  <ul class="list list-dark mt-4">
                      <h6>Keeping the above mentioned in mind. This is  our recommendation.</h6>  
                      <li><i class="fas fa-asterisk"></i>*First Semester and also mostly online  learning * - Due to Corona unfortunately you won't be able to attend classes physically and also you need  to get used to German education system , so do not rush  just take fewer courses/subjects or courses equivalent to 15 to 24 credits.(based on your confidence)</li>
                      <li><i class="fas fa-asterisk"></i>For some master programs like Medical systems, CEE you might have some mandatory subjects(courses) which might need to be taken in Semester 1 so take those and don't enroll anything extra if it crosses 22 to 24 credits limit   ].</li>
                      <li><i class="fas fa-asterisk"></i>In the past we have seen people enrolling for 6 or 7 courses(or 40+ credits ) , attending classes , doing assignments , etc the whole semester but finally during exams  they have dropped some courses and ended up just giving exams  just 3 or 4. So it is a wasted effort on the courses you dropped.</li>
                      <li><i class="fas fa-asterisk"></i> So register 5 or 6 courses based on your area of interest, attend lectures for initial two weeks and based on how comfortable you are choose 4 and focus on learning it.</li>
                      <li><i class="fas fa-asterisk"></i> From Second Semester onwards - As you are settled here and also know the German education system relatively better , probably  you might be in a position to decide what you want to pursue. You can  take courses and pace it as per your caliber and confidence (it can be 4 or 5 or 6 etc per semester ).</li>
                      <li><i class="fas fa-asterisk"></i>This will be based on your area of interest and your background knowledge in that area, for example if you already have prior knowledge in that area you spend relatively less time to learn so you can take an additional course in that semester.</li>
                      <li><i class="fas fa-asterisk"></i> Develop a habit of reading literature and text books. DON'T do last minute preparation ( which is useless you don't gain anything out of it and high chances of flunking), try to study from beginning and phase your studies by putting good effort and hard work then completing Masters will be easy like Rohit Sharma's effortless Six (Practice makes a (hu)man perfect)</li>
